export function getPresetById(id: string): PresetTheme | undefined {
  return PRESET_THEMES.find(theme => theme.id === id);
}

export function getPopularPresets(): PresetTheme[] {
  return PRESET_THEMES.filter(theme => theme.popular);
}

export function getPresetsByMode(mode: 'light' | 'dark'): PresetTheme[] {
  return PRESET_THEMES.filter(theme => theme.mode === mode);
}
